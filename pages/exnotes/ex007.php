<h3>Exercise 007 - while loops</h3>
<p>A while loop is normally used to loop <strong>while</strong> a condition is true. We will see more 
advanced examples in later tutorial but for now it is useful to understand that a while
loop is very similar to a for loop with a slightly different structure. Below is a 
while loop that does exactly the same thing as the for loop in the previous example.</p>

<pre>
<code>
	$i = 0;			<span class="rem">// The starting condition</span>
	while ($i < 10) {	<span class="rem">// The terminating condition in the ()</span>
		echo $i;
		$i++;		<span class="rem">// The operator</span>
	}
</code>
</pre>
