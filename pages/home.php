<h3>Getting Started</h3>

<p>These tutorial exercises must be placed on a functioning PHP server. For students
at the Chelmsford campus of Anglia Ruskin University this means that the tutorial
folder should be copied to your folder on ecommsvr5. For students at other institutions
your tutor will explain how your local system functions.</p>

<p>You can work on your tutorials from home using the Remote Desktop application
but a more convenient solution is to install a server environment on your own
computer. MAMP is a good choice and is available for both Mac OSX and Windows 
operating systems. If you are running Linux at home you should be able to install
a LAMP stack on your computer.</p>

<p>Each tutorial exercise is contained within its own directory labelled ex001, ex002
and so on. In most cases you simply need to edit the code in the index.php file. When 
this is not the case you will be given specific instructions. <strong>Do not</strong> 
change the directory structure within the tutorial folder.</p>
</article>

<aside>
<h3>Backing up</h3>
<p>You must back up your work regularly. Use a memory stick to copy your work to 
your home computer. Back up your work at least once per week.</p>
</aside>

<aside>
<h3>You don't need MAMP Pro</h3>
<p>The standard (free) version of MAMP will be fine.</p>
</aside>

<article>
<h3>Exercise Sets</h3>
<p>There are six Exercise Sets which should each take approximately two hours of 
<strong>continuous</strong> work to complete. If you do not complete a exercise 
set in the given tutorial period you are expected to complete the work in your 
own time before the next week's lecture. If you find that you complete a tutorial set
in less than two hours then you can move on to the next set.</p>

<p>Each exercise within a set will contain several components for you to complete. As 
you progress you will be given less specific instruction for each exercise. You are
expected to remember and apply what you have learnt in earlier exercises to later
problems.</p>
</article>

<aside>
<h3>Too easy? To Difficult?</h3>
<p>If you find the exercises too easy there are some extra, more comple exercises 
to test your knowledge. If you find an exercise difficult try to work through the 
problem and then ask your tutor for help.</p>
</aside>

