<h3>Exercise 005 - Associative Arrays</h3>
<p>We do not need to use numbers as the keys in an array - we can use strings 
instead. This is called an associative array. Here is an example...
</p>
<pre>
<code>
	<span class="rem">// Set up the array</span>
	$age['sara'] = 17;
	$age['tara'] = 18;
	$age['mata'] = 19;
</code>
</pre>
<p>
Note that the key (the girls name) is in single quotes. If you do not place the 
key in single quotes you will get an error.
</p>

<p>
When we start working with databases (next year) we will use associative arrays 
all the time. You will also use them when working on the quiz section of the assignment.
</p>
