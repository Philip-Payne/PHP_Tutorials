<h3>Exercise 021 - Using User defined functions</h3>
<p>Using a user defined function in your code is exactly the same as using one of the 
PHP built in functions except there is no man page for you to read. You are reliant on 
the comments in the code for the user defined function. When you start to write your 
own functions it is very important that you comment your functions clearly.</p>

<p>
In this exercise you need to edit the file <code>/ex021/index.php</code> but you need to 
look at the code in the included functions file 
<code>/ex021/includes/functions.php</code> which will make clear the workings of the 
functions.
</P.

<p>
Follow the instructions in the tutorial files to complete the exercise.
