<h3>Exercise 024 - hex_to_rgb()</h3>
<p>For this exercise you will need to write a function that takes a HEX colour as the 
parameter in the format <code>#aabbcc</code> and converts that HEX value to an array of 
decimal numbers.</p>

<p>
You will need to use the <code>str_replace()</code> function which we have 
covered previously along with the <code>str_split()</code> and <code>hexdec()</code> 
functions which we have not. You will need to look up the new functions yourself - 
remember to use the PHP man pages before resorting to other sources.
</p>
